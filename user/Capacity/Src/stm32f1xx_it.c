/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */
	if((__HAL_UART_GET_FLAG(&huart3, UART_FLAG_IDLE) != RESET) && __HAL_UART_GET_IT_SOURCE(&huart3, UART_IT_IDLE) != RESET)
	{
		__HAL_UART_CLEAR_IDLEFLAG(&huart3);
		(void)huart3.Instance->SR;
		(void)huart3.Instance->DR;

		HAL_UART_DMAStop(&huart3);		/*停用DMA（上面是清除串口中断）*/
		(void)hdma_usart3_rx.Instance->CNDTR;
		HAL_UART_Receive_DMA(&huart3,usart3_dma_buff,BUFFLEN);		/*再次开启，实现DMA复位重新接收*/
		/**************************/
		if((usart3_dma_buff[0] == 0xaa)&&(usart3_dma_buff[7] == 0x55))
		{
			if(usart3_dma_buff[3] != 0x00)SupperCapacitorSwitch = 1;
			else SupperCapacitorSwitch = 0;
			
			if(SupperCapacitorSwitch == 1)HAL_GPIO_WritePin(LED_GPIO_Port,LED_Pin,GPIO_PIN_RESET);
			else HAL_GPIO_WritePin(LED_GPIO_Port,LED_Pin,GPIO_PIN_SET);
			Chassis_Power_Last = Chassis_Power;
			Chassis_Power = usart3_dma_buff[4]<<8|usart3_dma_buff[5];
			Speed = usart3_dma_buff[6] << 9;
		}
		
		/**************************/
#ifdef HAL_UART_IRQHandler
  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */
#endif
	}
  /* USER CODE END USART3_IRQn 1 */
}

